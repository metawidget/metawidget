		</p>
		<p>
			The past 30 months have been an exciting ride. We've paid close attention to your feedback, conducted
			detailed interviews and case studies, and assisted over a dozen production deployments. We've
			seen Metawidget deployed at energy companies, by governments, in pharmaceutical labs,
			for ERP, for light and sound engineering, for telecommunications, and more.
		</p>
		<p>
			We've worked closely with key industry players and developed strong ties with the
			academic community - <a href="blogs.php">publishing conference papers and journal articles</a>, and
			fostering relationships with other research teams.
		</p>
		<p>
			We'd like to thank everyone for their support in getting us this far. And now that we're 1.0,
			there's never been a better time to dive in and see what Metawidget can do for you! It's mature,
			it's Open Source, and it's ready to save you from all that error-prone, laborious, boilerplate
			UI code! 
		</p>

		<p>
			This release was focused on:  
		</p>
		
